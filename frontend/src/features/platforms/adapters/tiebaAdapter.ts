export const tiebaAdapter: PlatformAdapter<TiebaRawItem> = {
  platform: 'tieba',
  displayName: '贴吧',
  iconText: '贴',
  color: '#0958d9',
  supportedChannels: ['public_comment', 'private_message'],
  supportedContentTypes: ['comment', 'reply', 'thread_reply', 'dm_message', 'conversation_message'],
  supportedSourceModes: ['mock', 'api', 'manual_import'],
  connectionSchema: [
    {
      key: 'mode',
      label: '接入模式',
      description: '贴吧接入建议使用后端受控同步或人工导入，私信内容默认视为高敏感。',
      type: 'select',
      options: [
        { label: 'Mock', value: 'mock' },
        { label: 'API', value: 'api' },
        { label: 'Manual Import', value: 'manual_import' },
      ],
    },
  ],
  metadataFields: [
    { key: 'forumName', label: '吧名' },
    { key: 'floor', label: '楼层' },
    { key: 'threadTitle', label: '主题帖标题' },
    { key: 'threadRelation', label: '楼层关系' },
    { key: 'conversationLabel', label: '会话标签' },
  ],
  riskHints: [
    {
      title: '贴吧水贴与顶帖',
      description: '注意短文本刷楼、重复顶帖和楼层诱导外链。',
      severity: 'medium',
    },
    {
      title: '贴吧私信资源轰炸',
      description: '低价资源、联系方式轰炸和外链投递需要重点拦截。',
      severity: 'high',
    },
  ],
  normalize(input) {
    return createBaseMockItem({
      id: input.id,
      platform: 'tieba',
      channel: input.channel,
      contentType: input.contentType,
      contentText: input.text,
      authorName: input.authorName,
      authorId: input.authorId,
      publishTime: input.publishTime,
      sourceUrl: input.sourceUrl,
      tags: ['贴吧'],
      riskSignals: input.riskSignals,
      targetAuthorName: input.targetAuthorName,
      targetAuthorId: input.targetAuthorId,
      receiverName: input.receiverName,
      receiverId: input.receiverId,
      conversationId: input.conversationId,
      messageId: input.messageId,
      direction: input.direction,
      isFirstContact: input.isFirstContact,
      platformMetadata: input.metadata,
    });
  },
  createMockItems() {
    return rawItems.map((item) => this.normalize(item));
  },
  getMetadataDisplay(item) {
    return this.metadataFields
      .map((field) => {
        const raw = item.platformMetadata[field.key];
        if (raw === undefined || raw === null || raw === '') return null;
        return { label: field.label, value: String(raw) };
      })
      .filter(Boolean) as Array<{ label: string; value: string }>;
  },
};
