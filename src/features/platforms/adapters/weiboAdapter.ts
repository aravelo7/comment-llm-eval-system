export const weiboAdapter: PlatformAdapter<WeiboRawItem> = {
  platform: 'weibo',
  displayName: '微博',
  iconText: '微',
  color: '#d4380d',
  supportedChannels: ['public_comment', 'private_message'],
  supportedContentTypes: ['post', 'comment', 'reply', 'dm_message', 'conversation_message'],
  supportedSourceModes: ['official_api', 'manual_import', 'web_session_import'],
  connectionSchema: [
    {
      key: 'mode',
      label: '接入模式',
      description: '仅支持 mock、合规 API 或人工导入，不提供任何绕过平台保护的采集方式。',
      type: 'select',
      options: [
        { label: 'Official API', value: 'official_api' },
        { label: 'Manual Import', value: 'manual_import' },
        { label: 'Web Session Import', value: 'web_session_import' },
      ],
    },
    {
      key: 'authorizationStatus',
      label: '凭证状态',
      description: '前端只展示状态，真实凭证必须由后端托管。',
      type: 'readonly',
    },
  ],
  metadataFields: [
    { key: 'topic', label: '话题' },
    { key: 'mentions', label: '@提及' },
    { key: 'interactions', label: '互动数' },
    { key: 'client', label: '来源客户端' },
    { key: 'conversationLabel', label: '会话标签' },
  ],
  riskHints: [
    {
      title: '微博评论区导流',
      description: '重点关注转评赞互导、主页留联系方式和群号引流。',
      severity: 'high',
    },
    {
      title: '微博私信高敏感',
      description: '私信接入仅允许在合法授权和后端受控同步前提下启用。',
      severity: 'high',
    },
  ],
  normalize(input) {
    return createBaseMockItem({
      id: input.id,
      platform: 'weibo',
      channel: input.channel,
      contentType: input.contentType,
      contentText: input.text,
      contentHtml: input.contentHtml,
      authorName: input.authorName,
      authorId: input.authorId,
      publishTime: input.publishTime,
      sourceUrl: input.sourceUrl,
      tags: ['微博'],
      riskSignals: input.riskSignals,
      targetAuthorName: input.targetAuthorName,
      targetAuthorId: input.targetAuthorId,
      receiverName: input.receiverName,
      receiverId: input.receiverId,
      conversationId: input.conversationId,
      messageId: input.messageId,
      direction: input.direction,
      isFirstContact: input.isFirstContact,
      platformMetadata: input.metadata,
    });
  },
  createMockItems() {
    return rawItems.map((item) => this.normalize(item));
  },
  getMetadataDisplay(item) {
    return this.metadataFields
      .map((field) => {
        const raw = item.platformMetadata[field.key];
        if (raw === undefined || raw === null || raw === '') return null;
        return {
          label: field.label,
          value: Array.isArray(raw) ? raw.join('、') : String(raw),
        };
      })
      .filter(Boolean) as Array<{ label: string; value: string }>;
  },
};
